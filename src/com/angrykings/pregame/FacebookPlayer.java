package com.angrykings.pregame;


public class FacebookPlayer {

    public String name;
    public int id;
    public String win;
    public String lose;
    public String fbID;

    public FacebookPlayer(String name, int id, String win, String lose, String fbID){
        this.name = name;
        this.id = id;
        this.win = win;
        this.lose = lose;
        this.fbID = fbID;
    }

}
